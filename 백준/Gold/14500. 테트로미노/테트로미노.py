import sys

input = sys.stdin.readline

n, m = map(int, input().split())
board = [list(map(int, input().split())) for _ in range(n)]

tetrominoes = [
    # I (2)
    [(0,0),(0,1),(0,2),(0,3)],
    [(0,0),(1,0),(2,0),(3,0)],
    # O (1)
    [(0,0),(0,1),(1,0),(1,1)],
    # L / J (8)
    [(0,0),(1,0),(2,0),(2,1)],
    [(0,1),(1,1),(2,1),(2,0)],
    [(0,0),(0,1),(0,2),(1,0)],
    [(0,0),(0,1),(0,2),(1,2)],
    [(0,0),(1,0),(1,1),(1,2)],
    [(0,2),(1,0),(1,1),(1,2)],
    [(0,0),(1,0),(2,0),(0,1)],
    [(0,0),(0,1),(1,1),(2,1)],
    # S / Z (4)
    [(0,0),(0,1),(1,1),(1,2)],
    [(0,1),(1,0),(1,1),(2,0)],
    [(0,1),(0,2),(1,0),(1,1)],
    [(0,0),(1,0),(1,1),(2,1)],
    # T (4)
    [(0,0),(0,1),(0,2),(1,1)],
    [(0,1),(1,0),(1,1),(2,1)],
    [(1,0),(1,1),(1,2),(0,1)],
    [(0,0),(1,0),(2,0),(1,1)],
]

ans = 0
for i in range(n):
    for j in range(m):
        for shape in tetrominoes:
            total = 0
            ok = True
            for dx, dy in shape:
                x, y = i + dx, j + dy
                if not (0 <= x < n and 0 <= y < m):
                    ok = False
                    break
                total += board[x][y]
            if ok and total > ans:
                ans = total

print(ans)
